let number1 = 3
let number2 = 9
let operation = "-"
if (operation === "+") console.log (`${number1} + ${number2}=${number1+number2}`);
else if (operation === "/") console.log (`${number1} / ${number2}=${number1/number2}`);
else if (operation === "*") console.log (`${number1} * ${number2}=${number1*number2}`)
else if (operation === "-") console.log (`${number1} - ${number2}=${number1-number2}`)